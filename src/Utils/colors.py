class Colors:
    # ANSI escape codes for text colors

    # Reset color to default
    RESET = "\033[0m"

    # Black text
    BLACK = "\033[30m"

    # Red text
    RED = "\033[31m"

    # Green text
    GREEN = "\033[32m"

    # Yellow text
    YELLOW = "\033[33m"

    # Blue text
    BLUE = "\033[34m"

    # Magenta text
    MAGENTA = "\033[35m"

    # Cyan text
    CYAN = "\033[36m"

    # White text
    WHITE = "\033[37m"
